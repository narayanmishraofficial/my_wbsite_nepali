// FORM VALIDATION
// ============================================
const contactForm = document.getElementById('contactForm');
if (contactForm) {
    contactForm.addEventListener('submit', function(e) {
        e.preventDefault();
        const inputs = contactForm.querySelectorAll('input, textarea');
        let valid = true;
        inputs.forEach(function(input) {
            if (input.hasAttribute('required') && !input.value.trim()) {
                input.classList.add('is-invalid');
                valid = false;
            } else {
                input.classList.remove('is-invalid');
            }
        });
        if (valid) {
            const msg = currentLang === 'ne' ? '✅ तपाईंको सन्देश पठाइयो! धन्यवाद।' : '✅ Your message has been sent! Thank you.';
            alert(msg);
            contactForm.reset();
        } else {
            const msg = currentLang === 'ne' ? '⚠️ कृपया सबै आवश्यक फिल्डहरू भर्नुहोस्।' : '⚠️ Please fill in all required fields.';
            alert(msg);
        }
    });
}

