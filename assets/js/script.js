// TYPING EFFECT
// ============================================
function initTyping() {
    const typedText = document.getElementById('typed-text');
    if (!typedText) return;

    let phrases = translations[currentLang].typingPhrases || ['Designer'];
    let phraseIndex = 0, charIndex = 0, isDeleting = false;
    let typingTimeout;

    function typeEffect() {
        const current = phrases[phraseIndex];
        if (isDeleting) {
            typedText.textContent = current.substring(0, charIndex--);
            if (charIndex < 0) {
                isDeleting = false;
                phraseIndex = (phraseIndex + 1) % phrases.length;
                typingTimeout = setTimeout(typeEffect, 400);
                return;
            }
            typingTimeout = setTimeout(typeEffect, 50);
        } else {
            typedText.textContent = current.substring(0, charIndex++);
            if (charIndex > current.length) {
                isDeleting = true;
                typingTimeout = setTimeout(typeEffect, 1500);
                return;
            }
            typingTimeout = setTimeout(typeEffect, 80);
        }
    }

    window.restartTyping = function() {
        clearTimeout(typingTimeout);
        phrases = translations[currentLang].typingPhrases || ['Designer'];
        phraseIndex = 0;
        charIndex = 0;
        isDeleting = false;
        typeEffect();
    };

    typeEffect();
}

